package com.cscie97.authentication;

/**
 * CredentialType is an enum that represents different types of credentials
 */
public enum CredentialType {
	/**
	 * Credential type representing a voice print.
	 */
	VOICE_PRINT,

	/**
	 * Credential type representing a face print.
	 */
	FACE_PRINT,

	/**
	 * Credential type representing a password.
	 */
	PASSWORD
}
